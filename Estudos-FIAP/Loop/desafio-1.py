

# Desafio 1

num_alimentos = int(input('Informe quantos alimentos você consumiu hoje: '))


for x in range(num_alimentos):
    calorias = float(input(f'Informe o número de calorias do alimento {x + 1}: '))
    total_calorias = 0
    total_calorias += calorias
    
    print(f'Você consumiu um total de {total_calorias} calorias hoje')
    
    
